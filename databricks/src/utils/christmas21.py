import os
import binascii


def GetRandomIndex (x,seed=None):
    seed = (
        seed if seed is not None
        else int(binascii.hexlify(os.urandom(4)), 16))
    rs = np.random.RandomState(seed)
    return rs.randint(x)

def GetExpon(y=1):
  return np.random.exponential(scale=y,size=None)

def FlipCoin(x=[-1,1]):
  return np.random.choice(x,None,[0.5,0.5])

def FlipExpon(y=0.1,x=[-1,1]):
  return GetExpon(y)*FlipCoin(x)

def GetRandomFlip(list1,ch=[0.9,0.1],seed=None):
  seed = (
        seed if seed is not None
        else int(binascii.hexlify(os.urandom(4)), 16))
  rs = np.random.RandomState(seed)
  return rs.choice(list1,None,p=ch)

def GetRandomNormal(mu, std , seed =None):
  seed = (
        seed if seed is not None
        else int(binascii.hexlify(os.urandom(4)), 16))
  rs = np.random.RandomState(seed)
  return rs.normal(mu, std, None)


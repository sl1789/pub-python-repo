def GetYFinanceData (x):
    data = yf.download(x, period = 'max')
    S0=data.iloc[len(data)-1]["Adj Close"]
    data = data.rename(columns = {"Adj Close": "Adj_Close"})
    data['pct_change'] = data.Adj_Close.pct_change(1)
    data['log_return'] = np.log(data.Adj_Close / data.Adj_Close.shift(1))
    df2 = pd.concat([data['pct_change'], data['log_return']], axis = 1)
    df2 = df2.reset_index(level=['Date'])
    df2 = df2.drop(columns = ['Date'])
    df2.reset_index(level=0, inplace=True)
    df2 = df2.dropna()
    return S0,df2

